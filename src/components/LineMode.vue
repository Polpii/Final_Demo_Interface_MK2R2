<template>
  <div class="line" v-on:click="changeMode()">
    {{name}}
    <div :class="{ isOn: mode, isOff: !mode }">{{getStatus(mode)}}</div>
  </div>
</template>

<script>
export default {
  name: 'LineMode',
  data() {
    return {
      mode: this.$store.state.modeName,
    };
  },
  props: {
    modeName: String,
    name: String,
  },
  methods: {
    getStatus(mode) {
      if (mode) {
        return 'ON';
      }
      return 'OFF';
    },
    changeMode() {
      this.mode = !this.mode;
      this.$store.commit(this.modeName);
    },
  },
};
</script>

